package uti.parser;

import java.util.*;

public class LinkName {
   Vector names=new Vector();
   Vector tokens = new Vector();
   void addValue(String name, Token t)
   {
	   names.addElement(names);
	   tokens.addElement(t);
	   
   }
   int getCount()
   {
	   return names.size();
   }
   String getValue(int i)
   {
	   return (String)names.elementAt(i);
   }
}
